<?php
// Создаем многомерный массив с информацией о фильмах
$movies = array(
    array("название" => "Фильм 1", "рейтинг" => 8.7, "актеры" => array("Актер 1", "Актер 2", "Актер 3")),
    array("название" => "Фильм 2", "рейтинг" => 9.2, "актеры" => array("Актер 4", "Актер 5", "Актер 6")),
    array("название" => "Фильм 3", "рейтинг" => 8.9, "актеры" => array("Актер 7", "Актер 8", "Актер 9")),
    array("название" => "Фильм 4", "рейтинг" => 9.5, "актеры" => array("Актер 10", "Актер 11", "Актер 12")),
    array("название" => "Фильм 5", "рейтинг" => 8.5, "актеры" => array("Актер 13", "Актер 14", "Актер 15"))
);

// Сортируем фильмы по рейтингу в убывающем порядке
usort($movies, function($a, $b) {
    return $b['рейтинг'] <=> $a['рейтинг'];
});

// Получаем 3 фильма с наивысшим рейтингом
$topMovies = array_slice($movies, 0, 3);

// Выводим информацию о фильмах с наивысшим рейтингом
echo "Фильмы с наивысшим рейтингом:\n";
foreach ($topMovies as $movie) {
    echo $movie['название'] . " - " . $movie['рейтинг'] . "\n";
}
?>